<script>
  import { createEventDispatcher } from 'svelte';

  import Photo from './Photo.svelte';
  import Video from './Video.svelte';

  export let activeTab;
  export let activeUnit;
  export let activeSubUnit;
  export let structure;


  const dispatch = createEventDispatcher();

  function selectUnit(unit) {
    dispatch('selectUnit', {
      unit,
    });
  }

  function selectSubUnit(subUnit) {
    dispatch('selectSubUnit', {
      subUnit,
    });
  }

  function selectTab(tab) {
    dispatch('selectTab', {
      tab,
    });
  }

  $: hasNext = activeTab !== structure[structure.length - 1];
  $: hasPrev = activeUnit !== structure[0].units[0];

  function navigate(backward = false) {

    if (backward) {
      if (activeSubUnit) {
        const currentSubUnitIndex = activeUnit.subUnits.indexOf(activeSubUnit);
        const possiblePrevSubUnit = activeUnit.subUnits[currentSubUnitIndex - 1];

        if (possiblePrevSubUnit) {
          selectSubUnit(possiblePrevSubUnit);
          return;
        }
      }

      const currentUnitIndex = activeTab.units.indexOf(activeUnit);
      const possiblePrevUnit = activeTab.units[currentUnitIndex - 1];

      if (possiblePrevUnit) {
        selectUnit(possiblePrevUnit);
        return;
      }

      const currentTabIndex = structure.indexOf(activeTab);
      const possiblePrevTab = structure[currentTabIndex - 1];

      if (possiblePrevTab) {
        selectTab(possiblePrevTab);
      } else {
        console.warn('Cannot go back!');
      }

      return;
    }

    // forward

    if (activeSubUnit) {
      const currentSubUnitIndex = activeUnit.subUnits.indexOf(activeSubUnit);
      const possibleNextSubUnit = activeUnit.subUnits[currentSubUnitIndex + 1];

      if (possibleNextSubUnit) {
        selectSubUnit(possibleNextSubUnit);
        return;
      }
    }

    const currentUnitIndex = activeTab.units.indexOf(activeUnit);
    const possibleNextUnit = activeTab.units[currentUnitIndex + 1];

    if (possibleNextUnit) {
      selectUnit(possibleNextUnit);
      return;
    }

    const currentTabIndex = structure.indexOf(activeTab);
    const possibleNextTab = structure[currentTabIndex + 1];

    if (possibleNextTab) {
      selectTab(possibleNextTab);
    } else {
      console.warn('Cannot go forward!');
    }
  }
</script>

