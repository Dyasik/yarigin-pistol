            <h4>«ВИКИНГ» ПУТИНА</h4>

            <Photo
                filename="pistolet_yaryigina-z35uyaz0-1539501682.jpg"
            />

            <p>Русский пистолет хорошо ложится в мужскую руку от среднего до большого размера; отчасти из-за
            большого расстояния между спусковым крючком и спинкой рукоятки при стрельбе в режиме самовзвода.</p>
            <p>В конструкции защёлки магазина подумали о леворуких. Кнопка защёлки магазина, с цепким рифлением,
            расположенная сзади предохранительной скобы, может при необходимости переставляться на правую сторону
            рамки. С точки зрения эргономики поводов для критики защёлки магазина и останова затвора нет. Для
            нажатия кнопки защёлки, в зависимости от размера кисти руки, понадобится немного перехватиться, но не
            больше, чем у наиболее распространённых моделей европейского или американского производства. На
            стрельбище: при первом снаряжении магазина самый первый патрон, после спуска затвора с останова
            уткнулся в казённый срез ствола. Но этого больше не повторилось. В дальнейшем тестовый экземпляр
            работал абсолютно надёжно со всеми использованными видами патронов, включая и патроны с экспансивными
            пулями, и слабые варианты снаряжения для спортивной стрельбы.</p>
            <p>Особенно понравился сухой характер спуска при стрельбе без самовзвода, что в сочетании с хорошим
            прицелом обеспечивает хорошую кучность стрельбы, как при стрельбе с упора, так и при стрельбе с двух
